-- Recipe Management Database Schema
-- Run this script to create the database and populate it with sample data

CREATE DATABASE IF NOT EXISTS recipe_management;
USE recipe_management;

-- Create recipes table
CREATE TABLE recipes (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    cuisine VARCHAR(100) NOT NULL,
    rating DECIMAL(2,1) DEFAULT NULL,
    prep_time INT DEFAULT NULL,
    cook_time INT DEFAULT NULL,
    total_time INT DEFAULT NULL,
    description TEXT,
    serves VARCHAR(50),
    calories VARCHAR(20),
    carbohydrate_content VARCHAR(20),
    cholesterol_content VARCHAR(20),
    fiber_content VARCHAR(20),
    protein_content VARCHAR(20),
    saturated_fat_content VARCHAR(20),
    sodium_content VARCHAR(20),
    sugar_content VARCHAR(20),
    fat_content VARCHAR(20),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- Create indexes for better query performance
CREATE INDEX idx_cuisine ON recipes(cuisine);
CREATE INDEX idx_rating ON recipes(rating);
CREATE INDEX idx_total_time ON recipes(total_time);
CREATE INDEX idx_title ON recipes(title);

